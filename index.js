const quote = document.getElementById("quote_container");

let lastQuotes = [];

let curIndex;

document.getElementById("previous").addEventListener("click", () => {
    	
    if (curIndex === 0) curIndex = quotes.length - 1; else curIndex--;
    displayQuote(quotes[curIndex])
    lastQuotes.push(newQuoteIndex);
    if (lastQuotes.length > 10) lastQuotes.splice(0, 1);

});

document.getElementById("next").addEventListener("click", () => {

    if (curIndex === quotes.length - 1) curIndex = 0; else curIndex++;
    displayQuote(quotes[curIndex])
    lastQuotes.push(newQuoteIndex);
    if (lastQuotes.length > 10) lastQuotes.splice(0, 1);

});

document.getElementById("random").addEventListener("click", () => {
    
    const newQuoteIndex = getRandomQuoteIndex();
    curIndex = newQuoteIndex;
    displayQuote(quotes[newQuoteIndex])

});


const displayQuote = newQuote => {
    if (newQuote.type === "standard") {

        quote.innerHTML = `
            <span class="fancy_quote">„</span>
            <span class="quote_text">
                ${newQuote.quote}
            </span>
            <span class="fancy_quote">“</span>
            <p class="quote_name">
                -${newQuote.name}
            </p>
        `;

    } else if (newQuote.type === "dialog") {
        let html = "";

        for (part of newQuote.parts) html += 
        `
            <span class="dialog_name">${part.name}:</span>
            <span class="quote_text">${part.text}</span>
            <br />
        `;

        quote.innerHTML = html;

    }
}

const getRandomQuoteIndex = () => {
    let newQuoteIndex = 0;
    while (lastQuotes.includes(newQuoteIndex)) {
        newQuoteIndex = Math.floor(Math.random() * quotes.length)
    }
    lastQuotes.push(newQuoteIndex);
    if (lastQuotes.length > 10) lastQuotes.splice(0, 1);
    return newQuoteIndex
}

document.getElementById("random").dispatchEvent(new Event("click"));
